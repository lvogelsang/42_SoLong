int	ft_isalpha(int c)
{
	int	x;

	if ((c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
	{
		x = 1;
	}
	else
	{
		x = 0;
	}
	return (x);
}
